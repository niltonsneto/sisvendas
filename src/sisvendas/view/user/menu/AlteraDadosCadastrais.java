private void startCad(String cod){
 u.setCodigo(Integer.parseInt(cod));
query = entityManager.createNamedQuery("Usuario.findByCodigo");
         query.setParameter("codigo", u.getCodigo());
         List<Usuario> usrList = query.getResultList();
         u.setCodCargo(usrList.get(0).getCodCargo());
         nomeField.setText(usrList.get(0).getNome());
         emailField.setText(usrList.get(0).getEmail());
         foneField.setText(usrList.get(0).getFone());
         loginField.setText(usrList.get(0).getLogin());
         senhaOldField.setText(usrList.get(0).getSenha());
}
