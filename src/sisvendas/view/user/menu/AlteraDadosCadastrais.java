    private void confirmaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmaButtonActionPerformed
        u.getCodCargo();
        u.setNome(nomeField.getText());
        u.setEmail(emailField.getText());
        u.setFone(foneField.getText());
        u.setLogin(loginField.getText());
        u.setSenha(senhaNewField.getText());
        entityManager.merge(u);
        entityManager.getTransaction().begin();
        entityManager.getTransaction().commit();
        JOptionPane.showMessageDialog(null, "Dados alterados.", "SISVENDAS - Alteração", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_confirmaButtonActionPerformed

