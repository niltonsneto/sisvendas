private void showData(List resultado){
    Vector<String> header = new Vector<>();
     Vector dadosTabela = new Vector();
    header.add("Categoria");
    header.add("Total (R$)");
   
    for (Object o:resultado){
        CategoriaTotal v =  (CategoriaTotal) o;
        Vector<Object> linha = new Vector<>();
        linha.add(v.getCategoria());
        linha.add(v.getTotal());
        dadosTabela.add(linha);
    }
    tabelaMovimento.setModel(new DefaultTableModel(dadosTabela, header));
}

private List<CategoriaTotal> listaVenda(){
    Calendar c = Calendar.getInstance();
    String query = "select new sisvendas.bean.admin.CategoriaTotal(v.codProduto.codTipo.tipo,sum(v.total)) from Venda as v where v.data = :d group by v.codProduto.codTipo";
    Query q = entityManager.createQuery(query);
    q.setParameter("d", c.getTime());
       return  q.getResultList();
    
}
